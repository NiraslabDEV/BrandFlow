-- Enable necessary extensions
CREATE EXTENSION IF NOT EXISTS "uuid-ossp";

-- ============================================================================
-- PLATAFORMA (singleton, só super-admin)
-- ============================================================================

create table platform_settings (
  id smallint primary key generated by default as identity,
  constraint singleton_check check (id = 1),

  -- Seleção de gateway (alternável no painel)
  subscription_provider text not null check (subscription_provider in ('zumbopay','paysuite','mock')) default 'zumbopay',
  credits_provider text not null check (credits_provider in ('zumbopay','paysuite','mock')) default 'paysuite',

  -- (B) SEGREDOS de gateway/email — cifrados (Supabase Vault), nunca ao client nem em texto puro
  zumbopay_api_key text,
  zumbopay_webhook_secret text,
  zumbopay_wallet_id text,
  zumbopay_merchant_id text,
  paysuite_api_key text,
  paysuite_webhook_secret text,
  resend_api_key text,

  -- (A) IDs PÚBLICOS de tracking — vão ao client via get_public_tracking()
  gtm_container_id text,
  meta_pixel_id text,
  ga4_measurement_id text,
  gads_conversion_id text,
  gads_conversion_label text,

  -- (B) SEGREDOS de tracking — só server-side
  meta_capi_token text,
  gads_developer_token text,

  updated_at timestamptz not null default now()
);

-- Insert singleton row
insert into platform_settings (id) values (1);

-- RLS: platform_settings — sem policies: anon e authenticated comuns não acedem
alter table platform_settings enable row level security;

-- ============================================================================
-- TENANTS
-- ============================================================================

create table tenants (
  id uuid primary key default uuid_generate_v4(),
  name text not null,
  slug text not null unique,

  plan text not null check (plan in ('base','pro','premium')) default 'base',
  status text not null check (status in ('trial','active','past_due','canceled')) default 'trial',

  trial_ends_at timestamptz not null default (now() + interval '14 days'),
  grace_until timestamptz,   -- Período de graça quando past_due (1.2.5)
  deleted_at timestamptz,    -- Soft-delete (1.2.7)

  created_at timestamptz not null default now()
);

-- ============================================================================
-- MEMBERSHIPS
-- ============================================================================

create table memberships (
  id uuid primary key default uuid_generate_v4(),
  tenant_id uuid not null references tenants(id) on delete cascade,
  user_id uuid not null,  -- auth.users.id
  role text not null default 'owner' check (role in ('owner','admin','member')),
  created_at timestamptz not null default now(),

  unique(tenant_id, user_id)
);

-- ============================================================================
-- HELPER FUNCTIONS (depois de memberships existir)
-- ============================================================================

create or replace function public.auth_tenant_ids()
returns setof uuid language sql stable security definer set search_path = public as $$
  select tenant_id from memberships where user_id = auth.uid();
$$;

-- ============================================================================
-- RLS — tabelas com tenant_id (padrão 16.1)
-- ============================================================================

alter table tenants enable row level security;
create policy "tenant_isolation" on tenants for all to authenticated
  using (id in (select auth_tenant_ids()))
  with check (id in (select auth_tenant_ids()));

alter table memberships enable row level security;
create policy "tenant_isolation" on memberships for all to authenticated
  using (tenant_id in (select auth_tenant_ids()))
  with check (tenant_id in (select auth_tenant_ids()));

-- ============================================================================
-- RESTAURANTS
-- ============================================================================

create table restaurants (
  id uuid primary key default uuid_generate_v4(),
  tenant_id uuid not null references tenants(id) on delete cascade,
  name text not null,
  slug text not null,

  logo_url text,
  brand_colors jsonb,
  photos jsonb,

  open_hour int not null default 15,
  close_hour int not null default 23,
  timezone text not null default 'Africa/Maputo',

  deleted_at timestamptz,
  created_at timestamptz not null default now(),

  unique(tenant_id, slug)
);

alter table restaurants enable row level security;
create policy "tenant_isolation" on restaurants for all to authenticated
  using (tenant_id in (select auth_tenant_ids()))
  with check (tenant_id in (select auth_tenant_ids()));

-- ============================================================================
-- CREDIT_WALLETS
