  created_at timestamptz not null default now()
);

alter table event_log enable row level security;
create policy "tenant_isolation" on event_log for all to authenticated
  using (tenant_id in (select auth_tenant_ids()))
  with check (tenant_id in (select auth_tenant_ids()));

-- ============================================================================
-- RPC: create_tenant
-- Cria atomicamente tenant + membership + restaurant + credit_wallet
-- ============================================================================

create or replace function public.create_tenant(p_name text, p_user_id uuid)
returns uuid language plpgsql security definer set search_path = public as $$
declare
  v_tenant_id uuid;
  v_restaurant_id uuid;
  v_wallet_id uuid;
  v_membership_id uuid;
begin
  insert into tenants (name, slug, plan, status, trial_ends_at)
  values (
    p_name,
    lower(regexp_replace(p_name, '[^a-z0-9]+', '-', 'g')) || '-' || substr(md5(random()::text), 1, 8),
    'base',
    'trial',
    now() + interval '14 days'
  )
  returning id into v_tenant_id;

  insert into memberships (tenant_id, user_id, role)
  values (v_tenant_id, p_user_id, 'owner')
  returning id into v_membership_id;

  insert into restaurants (tenant_id, name, slug, open_hour, close_hour, timezone)
  values (v_tenant_id, p_name, 'main', 15, 23, 'Africa/Maputo')
  returning id into v_restaurant_id;

  insert into credit_wallets (tenant_id, balance)
  values (v_tenant_id, 0)
  returning id into v_wallet_id;

  return v_tenant_id;
end;
$$;

grant execute on function public.create_tenant(text, uuid) to authenticated;

